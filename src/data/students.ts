// Helper function to convert registration date/time to DD/MM/YY format
function formatRegDate(regDateTime: string): string {
  const [datePart] = regDateTime.split(' ');
  const [month, day, year] = datePart.split('/');
  const shortYear = year.slice(-2); // Get last 2 digits of year
  return `${day.padStart(2, '0')}/${month.padStart(2, '0')}/${shortYear}`;
}

// Helper function to extract time from registration datetime
function formatRegTime(regDateTime: string): string {
  const [, timePart] = regDateTime.split(' ');
  return timePart;
}

// Helper function to clean phone numbers
function cleanPhoneNumber(phone: string): string {
  // Remove +234 prefix and replace with 0
  let cleaned = phone.replace(/^\+234/, '0');
  // Remove 0091 prefix (international format) and replace with 0
  cleaned = cleaned.replace(/^0091/, '0');
  // Remove any spaces
  cleaned = cleaned.replace(/\s/g, '');
  // Remove & and everything after it (for multiple numbers)
  cleaned = cleaned.split('&')[0].trim();
  // Ensure it starts with 0
  if (cleaned.startsWith('7') || cleaned.startsWith('8') || cleaned.startsWith('9')) {
    cleaned = '0' + cleaned;
  }
  return cleaned;
}

